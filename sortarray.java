import java.util.Arrays;

public class sortarray {
    public static void main(String[] args){
        int[] arr={1,3,4,5,2,1,9,0,-1};
        Arrays.sort(arr);
        String[] arr1={"Pavan","Sciro","pojskj","hdgtufwb","dsugh"};
        Arrays.sort(arr);
        for(String str:arr1){
            System.out.println(str);
        }
        for(int i:arr){
            System.out.println(i);
        }
    }
    
}
